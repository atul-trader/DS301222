# 1. Big O - upper bond (worst case)
# lst = [6,3,57,8,8,9,9,0,4,2]
# input = 0-9

# f(n) <= c.g(n)
# n0 <= n
# c > 0

# f(n) = O(g(n))

# n => input
# f(n) => function/method/single line statement/formula/algo
# c => constant value - which is use to satisfy the equation
# g(n) => time complexity (worst)
# t => time
# n0 => the threshold after which the input doesn't matter as the graph pattern will remain after the threshold point






# 2. Big Omega (Ω)
