# Types of types
# 1. Text File (ascii)
# 2. Binary File (images,audio,video)

# Modes for text files (bydefault mode - r)
# r   --> read mode, reads the data from file. 

# w   --> write mode, it creates the file if the file doesn't exist. 
#         If file already exist, then it replaces it 
#         also allows you to write the data in the file

# a   --> append mode, it creates the file if the file doesn't exist.
#         If file already exist, then it appends the data into it. 

# r+  --> r+ mode, it allows to read the data as well as write the data in the file

# w+  --> w+ mode,  it creates the file if the file doesn't exist. 
#         If file already exist, then it replaces it.
#         also allows you to write the data in the file
#         allows to read the data of the file

# a+  --> append mode, it creates the file if the file doesn't exist.
#         If file already exist, then it appends the data into it. 
#         allows to read the data of the file

